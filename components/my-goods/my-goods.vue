    }
  }
  // 右
  .goods-item-right {
    display: flex;
    flex-direction: column;
    justify-content: space-between;
    // 商品标题
    .goods-name {
      font-size: 13px;
    }
    // 价格文字
    .goods-info-box {
      color: #c00000;
