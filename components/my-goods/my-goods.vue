<template>
  <!-- 封装的商品组件 -->
  <view class="goods-item">
    <!-- 左侧的盒子 -->
    <view class="goods-item-left"><image class="goods-pic" :src="goods.goods_small_logo || defaultPic"></image></view>
    <!-- 右侧的盒子 标题 价格 -->
    <view class="goods-item-right">
      <!-- 有些图片没有了，我们定义一个默认的图片，用来加载 -->
      <view class="goods-name">{{ goods.goods_name }}</view>
      <!-- 用过滤器过滤价格，保留2位小数 -->
      <view class="goods-info-box">￥{{ goods.goods_price | tofixed }}</view>
    </view>
  </view>
</template>

<script>
export default {
  props: {
    goods: {
      type: Object,
      default: {}
    }
  },
  data() {
    return {
      // 默认的空图片
      defaultPic: 'https://img3.doubanio.com/f/movie/8dd0c794499fe925ae2ae89ee30cd225750457b4/pics/movie/celebrity-default-medium.png'
    };
  },
  filters: {
    // 把数字处理为带两位小数点的数字
    tofixed(num) {
      return Number(num).toFixed(2);
    }
  }
};
</script>

